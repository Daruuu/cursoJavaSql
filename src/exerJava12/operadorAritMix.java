package exerJava12;

public class operadorAritMix {

    public static void main(String[] args) {
        int numEntero = 25;

        System.out.println(numEntero += 5);
        System.out.println(numEntero -= 5);
        System.out.println(numEntero *= 2);
        System.out.println(numEntero /= 2);
        System.out.println(numEntero %= 3);

    }
}
