package exerJava07;

public class ExerJava7 {
    public static void main(String[] args) {
        final boolean mayorEdad = true;
        //caracter
        final char ch = 'R';
        // numeros enteros
        final short varShort = -30;
        // numero reales
        final int varNumero = 1240;
        final float varFloat = 40f;
        final double varDouble = 40.1;
        // constantes
        final Integer NUMEROFINAL = 2020;

        System.out.println(mayorEdad);
        System.out.println(ch);
        System.out.println(varShort);
        System.out.println(varNumero);
        System.out.println(varFloat);
        System.out.println(varDouble);
        System.out.println(NUMEROFINAL);
    }
}
