package exerJava05;

public class ExerJava05 {
    public static void main(String[] args) {
        //logicos
        boolean mayorEdad = true;
        //character
        char dollar = '$';
        // numeros enteros
        short varShort = -127;
        int varNumero = 1240;

        // numero reales
        float varFloat = 40f;
        double varDouble = 40.1;
        // constantes
        final int NUMCONST = 2020;

        System.out.println(mayorEdad);
        System.out.println(dollar);
        System.out.println(varShort);
        System.out.println(varNumero);
        System.out.println(varFloat);
        System.out.println(varDouble);
        System.out.println(NUMCONST);

    }
}
